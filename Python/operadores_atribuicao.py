# Declarando as variaveis
saldo1 = 5000
saldo2 = 8000

# Imprimindo as variaveis
print(saldo1)
print(saldo2)

# Usando a operador aritmetico de adição (+=)
saldo2 += saldo1
print(saldo2)

# Usando as operações de adição (+=) e subtração (-=)
saldo3 = 2000
saldo4 = 5000 

saldo4 += saldo3
print(saldo3)

saldo3 -= saldo4
print(saldo4)

# Usando a multiplicação (*=) 
saldo5 = 30
saldo6 = 5

saldo5 *= saldo6
print(saldo5)

# Usando a divisão (/=)
saldo7 = 40
saldo8 = 3

saldo7 /= saldo8
print(saldo7)

# Usando a divisão inteira (//=) Ela sempre trunca o MENOR valor mais proximo.
saldo7 //= saldo8
print (saldo7)